<h2>Fertigkeitswerte/Attribute</h2>

<ul>
	<li class="question">Woher weiß ich meine genauen Fertigkeits- und Attributswerte?</li>

	<li class="answer">Ihr könnt sie nie genau sehen. Wie im richtigen Leben, könnt ihr euch zwar
	mit anderen Messen und euch selbst abschätzen, werdet aber nie genaue Aussagen bekommen. Die
	umgefähren Angaben seht ihr in den Balkendiagrammen der Fertigkeitsliste. Stellt euch vor,
	vor euch auf der Schulbank unterhalten sich zwei Schüler darüber, daß sie gestern ihre
	Intelligenz um zwei gesteigert haben und eine Fertigkeit von 43 in Fahrradfahren haben.
	Lächerlich, oder? Um solche Diskussionen, die andere in ihrem Rollenspiel stören, zu
	vermeiden, gibt es hier keine Zahlenwerte. Es ist nämlich unheimlich nervig, wenn man sich
	gerade auf dem Marktplatz im Spiel mit jemanden über die Politik des Königs unterhält,
	während sich nebenbei Spieler Zahlenwerte an den Kopf knallen. Für die, die Rollenspiel
	betreiben, ist es dann schwer nicht selbst aus ihrer Rolle zu gleiten, doch für diese Spieler
	haben wir das Spiel geschaffen.</li>
</ul>

