<h2>Waffen/Rüstungsstärken</h2>

<ul>
	<li class="question">Woher weiß ich, welche Waffe wie gut ist?</li>

	<li class="answer">Wir versuchen in diesem Spiel eine "realistische" Fantasy
	Umgebung zu simulieren. Ihr sollt in eurer Rolle spielen und keine statistischen Daten
	auswerten. Deshalb haben wir die Werte von Waffen und Rüstungen vor euch versteckt. Ihr könnt
	sicher sein, daß Waffen und Rüstungen durch mehrere Parameter bestimmt sind und sich ohnehin
	nicht auf einen Wert reduzieren lassen. Ein Schmied im Mittelalter wußte auch nicht, daß der
	eine Dolch einen Angriffswert von 5, der andere von 6 hatte. Er konnte aber sehr wohl im
	Kampf sehen, welcher wirksammer war, und genau das könnt ihr im Spiel auch machen. Einige
	werden jetzt sagen, man sollte wenigstens umgefähr sehen, wie gut etwas ist, aber daß eine
	Helebarde mehr Schaden anrichtet als ein Dolch, und das Kurzschwert dazwischen liegt (Dolch
	ist aber natürlich schneller als die Helebarde), sollte sich eigentlich auch jeder selbst
	überlegen können</li>
</ul>

