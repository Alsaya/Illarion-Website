<h2>RPG/Rollenspiel?</h2>

<ul>
	<li class="question">Hier wird dauernd von "Rollenspielen" und "RPG"
	geredet. Was ist denn das?</li>

	<li class="answer">Rollenspiele sind Spiele, in denen man sich in eine andere Person
	hineinversetzt, und wie diese handelt. In unserem Fall stellt ihr euch also vor, eine Person
	in einer mittelalterlichen Phantasiewelt zu sein. Das könnte ein großer Held sein, ein
	mächtiger Magier, oder auch ein Bettler in den dunklen Straßen, oder ein Bauer, der von
	seinem Lehensherren geknechtet wird sein. Ihr werdet mit anderen Bewohnern dieser Welt in
	Kontakt treten, und sollt sie in euer Spiel mit einbeziehen und ebenso auf ihr Spiel
	eingehen. Wenn ihr mit anderen redet, versucht euch eine passende Sprache zuzulegen. Wenn ihr
	einen Baron spielt, redet ruhig etwas geschwollen, als Bauer gerne in derber Sprache. Ihr
	werdet sehen, es wird euch Spaß machen. Eine Quelle für Rollenspiel kann z.B. das RPG-Board
	auf dieser Webseite sein. Dort können Aktionen angestoßen werden, die ihr mit anderen
	Spielern durchführen wollt. Es können sich dort z.B. Intrigien und Kriege zwischen Gilden
	anbahnen, ihr könnt zu eurer Hochzeitsfeier einladen oder Hilfe bei der Suche nach euren
	Verlorenen Eltern suchen. Sicherlich werden einige Spieler darauf eingehen, und es bahnen
	sich ein paar Lustige Stunden für alle Beteiligten an. Wenn ihr dort also etwas schreibt,
	versucht anderen die Möglichkeit zu geben, an eurer Posting anzuknüpfen und reagiert dann
	wieder auf ihre Antwort. Schreibt auf dem RPG Board nur, was eurer Charakter schreiben würde,
	und redet auch im Spiel, soweit möglich, nur durch ihn. Dies nennt man dann IC (in
	character). OOC (Out of character) ist dagegen alles, was ihr als Spieler, also als die
	Person, die vor dem Computer sitzt, von euch gebt. Wenn ihr dies alles bis hier
	nachvollziehen konntet, werdet ihr sicher verstehen, wieso in diesem Spiel einiges anders ist
	als in anderen Spielen, die sich vielleicht auch RPG nennen. Vieles davon erschien euch
	vielleicht bis jetzt dumm, doch es dient dazu, hier das Rollenspiel besonders zu fördern
	(siehe auch Informationen->Über das Projekt). Wenn ihr trotzdem Probleme damit habt, gibt
	es sicher andere Spiele, die euch mehr Spaß machen. Schaut dann mal im Linkbereich, wo auch
	einige andere Spiele und Spielelisten zu finden sind.</li>
</ul>

