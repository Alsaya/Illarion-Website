<h2>Chatfunktionen</h2>

<ul>
	<li class="question">Wie kann ich anderen Spielern im Spiel eine Botschaft zukommen lassen
	oder Nachrichten an alle schicken?</li>

	<li class="answer">Im Mittelalter gab es keine Handys. Eventuell werden wir sehr erfahrenen
	Magiern die Fähigkeit geben, mit anderen mental in Kontakt zu treten, oder sogar zu allen zu
	sprechen. Diese Magier könnten ihre Dienste dann gegen bare Münze zur Verfügung stellen. Es
	gab auch einmal die Idee, daß sich jeder Spieler Brieftauben kaufen kann, die dann für ihn
	Botschaften überbringen. Vielleicht werden wir das auch einbauen, die Brieftaube wird aber
	lange unterwegs sein und euch solange nicht zur Verfügung stehen.</li>
</ul>

