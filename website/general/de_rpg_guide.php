<p><a name="15"></a></p>

<h2>Der falsche Pfad</h2>

<p>Denk immer daran, das Ziel in Illarion ist es, durch sein Rollenspiel Spaß zu haben. Wenn du
andere Ziele im Auge hast, solltest du dir überlegen, ob nicht ein anderes Spiel für dich besser
geeignet ist oder du hier vielleicht sogar mit unseren Regeln in Konflikt kommst. Insbesondere
ist es nicht Ziel von Illarion möglichst viele Punkte in irgendeiner Form zu machen. Wessen Ziel
es also ist, seine Schmiedefertigkeiten in kürzester Zeit auf das Maximum zu bringen (obwohl er
Barkeeper von Beruf ist), oder wer als Magier der Meinung ist, sein Ziel läge darin, seine
Handhabung von Helebarden zu perfektionieren, sollte das Spiel lieber verlassen. Es gibt genug
andere Spiele, die solches Verhalten tolerieren, ja sogar wünschen und belohnen. Man verwendet
den Begriff "Powergaming" für jegliche solche Handlung, die nicht zum Charakter paßt
und nur mit dem Ziel ausgeführt wird, sich technische Vorteile zu verschaffen. Übt also der in
ehren ergraute Magier, der sich wegen seiner Gicht kaum noch fortbewegen kann, 24h am Tag
Helebardenkampf, widerspricht das zum einem seiner Veranlagung (Bewegung schmerzt ihn, er würde
lieber in der Bibliothek in Büchern stöbern, und außerdem braucht er ein paar Stunden Schlaf und
Pausenzeiten am Tag) und dient zum anderen nur dem erschleichen technischer Vorteile (er wird
nach ein paar Tagen solcher Spielweise die stärksten Untiere ohne den Einsatz von Magie
besiegen). Selbiges Verhalten mag aber einem jungen Krieger, der kurz vor seiner Abschlußprüfung
an der Kampfschule steht, gut zu Gesicht stehen (wenn er zwischendurch mal ein paar Essenspausen
macht und Schlafen geht). Man kann also bei den wenigsten Tätigkeiten allgemein sagen, daß es
Powergaming ist, ohne die Person zu betrachten, welche die Handlungen durchführt.</p>

