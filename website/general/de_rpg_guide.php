<p><a name="3"></a></p>

<h2>Dein Name</h2>

<p>Wähle dir einen Namen, wie er in einem Land wie Illarion vorkommen mag. SuperHero666 ist also
z.B. kein guter Name. Namen wie Gandalf und Aragorn klingen da schon eher passend - auf den
ersten Blick aber nur. Generell sind die Namen gut, wenn sie nur selber ausgedacht wären! Wenn
man Namen aus Büchern oder Filmen klaut, muß man sich einer Tatsache bewußt sein: Andere Spieler
kennen die Namen auch und haben eine gewisse Erwartung an die Person, die sich hinter dem Namen
verbirgt. Wenn sich jemand Gandalf nennt, gehen alle instinktiv davon aus, daß er ein Mensch,
Magier, alt, weise und mächtig ist. Ihr legt damit euren Charakter zu sehr fest, und verhindert
damit jegliche eigenständige Entwicklung des Charakters, denn sobald er sich von seinem Vorbild
entfernt, werden andere Spieler irritiert sein und dadurch in ihrem Rollenspiel gestört.
Versuche also bekannte Namen zu vermeiden.<br />
Wähle einen Namen, der zu deinem Volk paßt, einprägsam ist und sich aussprechen läßt. Einen
ganzen Regelsatz für Namen, der eingehalten werden muß, gibt es auf unser Webseite.</p>

