<p><a name="13"></a></p>

<h2>Technische Grenzen</h2>

<p>Versuche Grenzen der Software durch Phantasie zu überwinden. Nur weil es keine technische
Möglichkeit gibt, jemanden zu Fesseln, heißt das nicht, daß man es durch #me-Kommandos nicht
simulieren kann, wenn alle Beteiligten mitspielen. Nur weil es noch kein Bier gibt, heißt das
nicht, daß man nicht trotzdem in der Bar einen Heben kann. Wer betrunken ist, wird dann auch
selbstverständlich kleine Schlangenlinien laufen, auch wenn der Server das nicht vorgibt.<br />
In einem anderen online Spiel, was ich früher gespielt habe, gab es keine Möglichkeit Häuser zu
bauen. Wir stapelten Kisten als Mauern übereinander und streuten Mehl als Sandstrand aus. Das
Ergebnis war nachher eine Stadt mit mehreren Dutzend Einwohnern, Geschäften und einem Schloß,
daß sich über viele Bildschirme erstreckte (mit Türmen, Wehrgängen und allem Luxus
ausgestattet). Laßt euch also da nicht einschränken.<br />
Andererseits gibt es logische Grenzen, die aber technisch noch nicht realisiert sind.
Beispielsweise kann man mit einer Schaufel im Boden graben, obwohl sich diese Schaufel nicht in
der Hand, sondern im Rucksack befindet. Ebenso kann man permanent in vollen Rüstung und bis an
die Zähne bewaffnet umherstolzieren, ohne, daß es jemandem auffällt. Hier ist es an euch, euch
selbst eine Grenze zu setzen und z.B. beim Besuch eines Tempels die Waffen einzustecken, beim
Goldschmieden die Plattenrüstung abzulegen und die Schaufel zum Graben in die Hand zu
nehmen.</p>

