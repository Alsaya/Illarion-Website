<h2>Atmosphärenbrüche</h2>

<p>Stell dir vor, du siehst einen Film: Ein Kind geht im dunklen durch einen Wald nach hause.
Nebelschwaden liegen auf dem schmalen Pfad. Bäume knarren, Schatten von im Wind bewegten Ästen
formen Gestalten auf dem Erdboden. Das Kind singt zaghaft ein Lied, um seine Angst zu bekämpfen.
Doch plötzlich - ein Mikrophon ragt von oben ins Bild und ein Kabelträger läuft samt Kameramann
hinter dem Kind lang. Der Regisseur schreit "Kamera 3 aus! Mikro im Bild!".<br />
Bevor dieser Film in die Kinos kommen wird, wir diese Szene sicherlich noch mal neu gedreht und
nicht so belassen. Das gleiche gilt fürs Rollenspiel, nur daß wir hier nicht neu drehen können.
Es muß beim ersten mal sitzen. Das Mikrophon was ins Bild ragte ist in unserem Fall vielleicht
ein Charakter, dessen Spieler ihn plötzlich sagen läßt "Moment - mein Telephon klingelt -
ich bin in zwei Minuten zurück!". Viel schlimmer ist es noch, wenn eine Pilzverkäuferin
gerade auf dem Marktplatz einem ihrer Kunden ihr Leid klagt, daß sie wegen der neuen Steuern des
Fürsten kaum noch genug Geld hat, um ihrer kleinen kranken Tochter Medizin beim Druiden zu
kaufen und der Kunde daraufhin antwortet "Wo wir gerade von Medizin reden: weißt du eine
Webseite, wo ich einen kostenlosen Virenscanner bekomme?"</p>

