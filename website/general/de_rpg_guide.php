<p><a name="8"></a></p>

<h2>OOC Wissen / Charakter Trennung</h2>

<p>Wer sich aktiv am Spiel beteiligt, der wird sicherlich auch die Foren auf unserer Webseite
lesen. Dort erfährt man einiges an Informationen. Man sollte diese Informationen aber nicht im
Spiel ausnutzen, wenn sie dem Charakter nicht tatsächlich auch zur Verfügung stehen. Beschreibt
ein anderer Spieler am Forum z.B. wie sein Charakter sich in die Sitzung einer Gilde
einschleicht und diese belauscht, so ist das eine Information, die deinem Charakter sicher nicht
zur Verfügung steht. Schreibt allerdings jemand eine Preisliste für seinen Krämerladen auf das
Forum und ergänzt diese Liste um die Information, daß die Liste auf dem Dorfplatz am Fahnenmast
hängt, so kann dein Charakter sich dieser Information natürlich bedienen.<br />
Ähnliches gilt, wenn du mehrere Charaktere (abwechselnd) spielst. Dinge die der eine von ihnen
erfährt, stehen dem anderen nicht zur Verfügung. Es geht deshalb selten gut, zwei sehr aktive
Charaktere zu spielen. Ebenso wie Informationen sollten auch keine Waren oder Geld zwischen
beiden Charakteren getauscht werden. Es ist absolut unpassend, daß jemand einen Druiden spielt,
nur um mit diesem kostenlos Tränke für seinen anderen Charakter, der Magier ist,
herzustellen.</p>

