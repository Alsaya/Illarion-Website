<p><a name="1"></a></p>

<h2>Was ist Rollenspiel?</h2>

<p>Ein Rollenspiel ist wie ein Theaterstück, nur daß jeder mitspielen kann, und die Handlung
offen ist. Was als nächstes geschieht hängt im wesentlichen von deinem eigenem Verhalten ab. Es
geht also um Improvisation. Unsere Aufgabe als "Erschaffer" von Illarion ist es dabei,
eine Kulisse zur Verfügung zu stellen (die Spielwelt) und allen Spielern zu ermöglichen, sich in
diese Welt hineinzubegeben (durch das Aufstellen des Servers und zur Verfügung stellen der
Software). Eure Aufgabe als Spieler ist es, eine Person in diesem Theaterstück zu spielen und -
aus ihrer Sicht - schlüssig zu handeln. Da so eine Computersoftware euch in der Spielwelt
natürlich nur das ermöglicht, was die Programmierer vorgesehen haben, euch aber sicher mehr
einfallen wird, muß man an den Stellen, wo man an technische Grenzen stößt, diese durch
Phantasie überwinden, doch dazu später mehr.</p>

