<p><a name="10"></a></p>

<h2>Spieler/Charakter Ebene</h2>

<p>Lerne zwischen diesen beiden Ebenen zu trennen. Wenn ein Zwerg deinen Ork als stinkiges
Untier bezeichnet, ist das ein Konflikt zwischen dem Zwerg und dem Ork, nicht zwischen dem
Spieler des Zwerges und dem Spieler des Orks. Wechselt der Spieler des Orks zu einem anderen
Charakter, der ebenfalls Zwerg ist, können die beiden durchaus die besten Freunde sein. Diese
Trennung gilt auch für die Foren auf der Webseite. Aus der Nichtbeachtung dieser Ebenen
entstehen häufig unschöne Schlammschlachten auf den Foren.</p>

