<p><a name="16"></a></p>

<h2>Keine Lust mehr zu spielen?</h2>

<p>Am Ende eurer Kariere unterlaßt es bitte euren Charakter an andere Spieler weiterzugeben. Ihr
habt in eurem Leben viele Kontakte geknüpft und Freunde und Feind gefunden. Die haben sich an
eine bestimmtes Verhalten bei euch gewöhnt und auch gewisse Geschichten mit euch gemeinsam
erlebt. Wenn ihr euren Charakter weitergebt, wird der neue Besitzer weder die richtige
Verhaltensweise und Sprache an den Tag legen, noch sich an die gemeinsam durchlebten Geschichten
der Freunde erinnern. Kurz um, er kann sein altes Leben nicht korrekt weiterführen und verwirrt
dadurch die Mitspieler. Gleiches gilt für das kurzfristige Ausleihen von Charakteren.</p>

<?php insert_go_to_top_link(); ?>

<p><a name="17"></a></p>

<h2>Rache ohne Ende</h2>

<p>Charaktere wachsen einem mit der Zeit ans Herz. Stirbt ein Charakter also, bricht für viele
Spieler fast eine Welt zusammen. Deshalb haben wir den Tod in Illarion entschärft, so daß er
zwar schmerzlich ist, jedoch ein Weiterspielen unter gewissen Verlusten ermöglicht. Der einzige
permanente Tod droht euch durch die Gamemaster, wenn sie euch bei einem massiven Regelbruch
erwischen. Spieler sollten sich also nicht als das Gesetz aufschwingen um selber für Ordnung zu
sorgen. Wenn ihr selbst Rache an jemandem nehmt, und dazu, wegen der schwere der Schuld, der Tod
erforderlich ist, laßt die Tat dann mit dem Tod gesühnt sein. Es ist schwer logisch zu erklären,
wieso jemand zweimal wegen der selben Sache sterben muß, also tötet ihn nur ein zweites mal,
wenn er sich wieder etwas Neues zu schulden kommen läßt. Ganz nebenbei ist dieses mehrfache
töten ("Multi killing") wegen der selben Angelegenheit in den Regeln verboten und kann
damit Konsequenzen für euch haben.</p>

