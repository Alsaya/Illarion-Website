<p><a name="4"></a></p>

<h2>Kenne deinen Charakter!</h2>

<p>Da es kein Drehbuch gibt, gibt es auch keine vorgeschriebenen Rollen. Denke dir deshalb
selber eine Rolle aus, die du schon immer einmal verkörpern wolltest. Wichtig ist dabei nur, daß
sie in das Umfeld von Illarion paßt - ein Automechaniker wäre also unpassend, denkbar wäre aber
ein Wagenmacher, der Pferdegespanne und Eselkarren herstellt.<br />
Hast du dich einmal für eine Person entschieden, solltest du dir Gedanken über ihre
Lebensgeschichte bis dato machen. Aus ihr soll sich dann die Handlungsmotivation deiner
Spielfigur ableiten: Was sind ihre Ziele? Welche Abneigungen und Vorlieben hat sie?<br />
Wirst du in Gespräche verwickelt, mußt du ohne großes Nachdenken Rede und Antwort stehen. Wie
heißen also deine Eltern? Wo bist du geboren? Wie alt bist du? Wie sah dein Geburtshaus aus?
Welcher Religion gehörst du an? Wie redest du eigentlich? Bist du kurz angebunden oder weit
ausschweifend? Beherrschst du eine feine Ausdrucksweise oder verwendest du die rohe Sprache der
Knechte? Besitzt du Sprachfehler oder sprichst einen Dialekt?<br />
Denke beim Ausarbeiten deiner Spielfigur auch daran, daß eine Welt nicht nur aus Superhelden
besteht. Nicht jeder Spieler sollte ein Königssohn sein, genausowenig wie nicht jeder ein
Waisenkind ist, das seine verlorenen Eltern sucht.</p>

