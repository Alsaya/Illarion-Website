<p><a name="9"></a></p>

<h2>#me</h2>

<p>Da man am Bildschirm nur eingeschränkt sehen kann, was eine Person gerade tut, sollte man
diese Handlungen beschreiben. In Illarion wird dazu "#me" verwendet. Tippt der Spieler
von Udolpho z.B. "<strong>#me</strong> beugt sich zum Fluß herunter und benetzt seine Hände
mit Wasser" erscheint am Bildschirm "<strong>Udolpho</strong> beugt sich zum Fluß
herunter und benetzt seine Hände mit Wasser<strong>.</strong>".<br />
Damit soll aber nur beschrieben werden, was die Anderen wirklich sehen können. Die Aussage
"Udolpho denkt sich, was Melmoth doch für ein Blödmann ist." ist völliger Schwachsinn,
denn die Gedanken kann man ja schlecht sehen. Sinnvoll ist dagegen die Aussage "Udolpho
blickt zu Melmoth, wobei ein überhebliches Lächeln seine Lippen umspielt.".<br />
Die #me-Kommandos sind an vielen Stellen wichtig, um technische Mängel auszugleichen. Am
Bildschirm sieht und hört man z.B. nicht, wenn eine Waffe gezogen wird. Der Gegner hat deshalb
keinerlei Vorwarnzeit. Der Satz "Udolpho zieht sein Schwert und richtet es auf
Melmoth." ist hier angebracht.</p>

