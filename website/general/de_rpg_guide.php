<p><a name="7"></a></p>

<h2>Out of character (OOC) vs. In Character (IC)</h2>

<p>Mit IC bezeichnet man alles, was ein Charakter anderen Charakteren gegenüber äußert. Mit OOC
bezeichnet man jegliche Äußerung, die zwar der Charakter am Bildschirm von sich gibt, doch die
von seinem Spieler an einen anderen Spieler gerichtet ist. Die Frage, ob man 20 Äpfel gegen
einen Schafskäse eintauschen könnte, wäre demnach eine IC Äußerung, während die Aussage
"Ich muß Schluß machen, gleich kommt Star-Trek im Fernsehen." natürlich OOC gemeint
ist.<br />
OOC sollte grundsätzlich vermieden werden. Wenn es sich nicht vermeiden läßt (es soll z.B. einem
der Programmierer ein Fehler im Programm vorgeführt werden), sollte man das so kurz wie möglich
halten und dafür einen Ort aufsuchen, wo keine unbeteiligten Charaktere weiterhin ihr
Rollenspiel betreiben. Zusätzlich sollten OOC Äußerungen als solche markiert werden, um
Verwechslungen zu vermeiden. Markieren kann man sie, indem man den ganzen Satz z.B. in Klammern
setzt, oder "OOC: " vorne an stellt ( "((Wenn ich jetzt hier mit der Maus
hinklicke, stürzt er ab))" oder "OOC: Ich muß weg").<br />
Es empfiehlt sich für verschiedene OOC-Gelegenheiten Phrasen parat zu haben, die man den
Charakter sagen lassen kann, ohne ins OOC abzurutschen. Sagt man z.B. "Gandalf döst
ein", hat das den selben Effekt, wie wenn man schreibt "OOC: Moment - es klingelt an
der Tür". Oder man sagt "Ich schicke dir später einen Boten, wenn ich meine Antwort
überdacht habe" statt "Ich schick es dir per Email".</p>

