<p><a name="14"></a></p>

<h2>Immer freundlich?</h2>

<p>Weil wir unsere Spielerschaft hier handverlesen, sind die meisten so herzensgut, daß sie es
etwas übertreiben. Handelt so, wie es dem Gemüt eures Charakters entspricht, also bei den
meisten Personen sollte der eine oder andere Wutausbruch dazugehören - ohne Konflikte wäre es ja
langweilig. Eine weit verbreitete Unsitte ist es, neue Spieler reich zu beschenken. Dies hat
mehrere Nachteile, denn es nimmt dem beschenkten Spieler die Freude am Spiel, auch wenn er sich
zuerst darüber freut. Für viele Spieler ist es eine Quelle von Motivation, sich ihren
Besitzstand zu erarbeiten. Bekommen sie nun am ersten Tag eine komplette High-End Ausrüstung
geschenkt - was können sie noch weiter erreichen? Weiterhin wird durch diese Geschenke die
Wirtschaft durcheinander gebracht. Wenn teure Ausrüstung von einigen einfach verschenkt wird,
wovon sollen die fleißigen Handwerker dann leben? Die Preise fallen ja ins bodenlose. Wenn ihr
also einem neuen Spieler etwas Gutes tun wollt, dann beschäftigt euch mit ihm. Bezieht ihn in
euer Rollenspiel ein und gebt ihm Impulse, was er tun kann. Das Gute, was ihr ihm tut kann also
auch durchaus etwas Böses sein...<br />
Besonders störend ist es, wenn Charaktere, die eigentlich als "Böse" angelegt sind,
dann zwischendurch ihre dunklen Machenschaften an den Nagel hängen und sich mit dem gemeinen
Volk zusammen auf ein Bier in der Taverne treffen und dort freizügig ihre Todfeinde beschenken
und sich nett unterhalten oder ein Theaterstück auf dem Marktplatz besuchen. Am Tag darauf
ziehen sie dann wieder mordend und brandschatzend durch das Dorf und werden von jedermann
gejagt. Wenn ihr also einen "Bösen" spielen wollt, seid euch von Anfang an darüber im
klaren, daß ihr euch damit permanent aus der "normalen", friedliebenden Gesellschaft
ausschließt.</p>

