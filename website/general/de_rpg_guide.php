<p><a name="11"></a></p>

<h2>Eingehen auf anderer Leute Rollenspiel</h2>

<p>In einem Rollenspiel wie diesem sind alle deine Gefährten von echten Spielern gesteuert und
nicht von einem Computer. Daraus folgt, daß sie die gleichen Bedürfnisse und Rechte haben wie
du. Wenn du also von anderen Leuten erwartest, daß sie auf deine Handlungen reagieren, tue das
selbe für sie. Wenn jeder nur "sein Ding durchzieht", kann man gleich ein
Ein-Spieler-Computer-Rollenspiel spielen. Wenn sich also eine Gruppe von drei Leuten auf dich
stürzt und mit #me-Kommandos beschreibt, wie sie dich überwältigen und fesseln, spiele mit (auch
wenn es dir technisch möglich wäre, wegzugehen!). Wenn du besonders stark bist, kannst du ja auf
gleicher Weise einen Befreiungsversuch beschreiben, aber unterlasse es, sie vollständig zu
ignorieren, auch wenn es für dich unangenehm ist.<br />
Wenn du eine größer angelegte Aktion vor hast, ist es eine gute Idee, die vorher am Forum in
geeigneter Form anzukündigen, um vielleicht andere Spieler zur Teilnahme zu bewegen. Spielst du
z.B. in einer Räuberbande mit, die planen das Dorf zu überfallen, kann man Tage vorher schon
damit beginnen, kleine Berichte am Forum zu schreiben, wie verschiedene zwielichtige Personen in
der Nähe der Stadt herumlungern, die Wachen beobachten und im nahen Wald ein Lager aufschlagen.
Dies wird anderen Spielern die Möglichkeit zur Teilnahme geben, so daß an dem Ereignis
vielleicht 20 Personen Spaß haben, statt nur 5. Spaß haben muß hier nicht unbedingt
"gewinnen" heißen, denn das Ziel ist es ja nur Spaß am Rollenspielen zu haben. Man
kann also auch Spaß beim "verlieren" haben.</p>

