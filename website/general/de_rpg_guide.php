<h2>Böse Buben</h2>

<p>Die Charaktere, die am häufigsten schlecht gespielt werden sind "böse" Charaktere.
Sie sind besonders schwer zu spielen. Viele einfallslose Spieler wollen einfach nur andere Leute
totschlagen, wie in anderen Computerspielen. Dabei fehlt ihnen aber die Begründung, wieso sie
das tun und so werden sie einfach nur zum wahllosen Schlächter.<br />
Wer z.B. einen Mörder spielt, wird nicht wahllos jeden Töten, der ihm über den Weg läuft. Das
Risiko, dabei erwischt oder selbst getötet zu werden ist viel zu groß. Er wird nur dort Töten,
wo er ein Motiv hat und vor Rache relativ sicher ist. Außerdem sollte er von seinen
Charakterzügen so angelegt sein, daß ihm ein skrupelloser Mord überhaupt möglich ist. Auch ein
Wahnsinniger wird nicht wahllos töten. Auch er hat eine Geschichte, wie er zu dem Wahn kam, und
vielleicht bestimmte Situationen, die bei ihm einen Wutausbruch hervorrufen. Dies muß aber alles
glaubhaft begründet sein.<br />
Komischerweise denken die Meisten bei "böse" immer an Mörder, obwohl doch der Mord
wahrscheinlich das seltenste Verbrechen ist. Was ist denn mit Trickbetrügern? Dieben?
Raufbolden? Intriganten? Spionen? Wegelagerern? Erpressern? Entführern? Übler Nachrede gegen die
Handwerkerkonkurrenz? Nachbarschaftsstreitereien? Bauern, die der Konkurrenz Ungeziefer in ihr
Scheune setzen? Ist es nicht viel befriedigender eine Gilde über Monate hinweg zu infiltrieren,
ihre Geheimnisse zu verkaufen und die Gilde gegen eine andere aufzuhetzen, um damit eine
Konkurrenz für die eigene (dritte) Gilde aus dem Weg zu räumen?<br />
Was auch immer ihr für einen Typ von "Bösen" spielt, ihr solltet ihn konsistent
spielen. Nicht an einem Tag den fiesen Wegelagerer mimen und, sobald es attraktiv erscheint, am
Leben des normalen Bürgers teilzunehmen, einen handzahmen Bürger, der am Bankett des Königs
teilnehmen will (wo ihn natürlich viele als Schwerverbrecher erkennen, aber nichts gegen ihn
unternehmen, weil es ja so gemütlich ist, zusammen zu feiern).</p>

