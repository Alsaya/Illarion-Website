<p><a name="18"></a></p>

<h2>In Übung bleiben</h2>

<p>Damit du selbst das Gefühl fürs Rollenspiel nicht zwischendurch verlierst, mach auch damit
weiter, wenn gerade kein anderer Spieler in der Nähe ist. Beim Holzhacken kann man also ruhig
Stöhnen und sich den Schweiß abwischen, auch wenn niemand zugegen ist, der diese
schauspielerische Glanzleistung beobachtet. Wenn du es nicht machst, wirst du in einen Trott
