<p>In the year three of the new era on september, the 14th, Kincent Noltari makes a proposal to end the drought combining prayers and magic, which means divine energy and mana.</p>

<p>In the year three of the new era on september, the 14th, Kasume was thinking in public about suicide, but was detained by Kincent Nolatri and Aristeaus.</p>

