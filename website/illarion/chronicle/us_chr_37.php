<p>In the year three of the new era on september, the 6th, Gnarr is looking for a savant, who can tell him more about the godess Zelphia.</p>

<p>In the year three of the new era on september, the 7th, Dyluck starts a poll to clear the question of if the court procedures are long enough. Most people say that five days is a sufficient amount of time.</p>

<p>In the year three of the new era on september, the 8th, Elaralith apologizes for her deeds and makes a proposal of a common prayer of all, to end the drought and to avenge the defilement of Falkner's Shrine. Fierce discussions about herself come up. Aragon supports the suggestion about the prayer.</p>

