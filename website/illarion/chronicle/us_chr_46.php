   <h2>The fourth year of the new era: June</h2>

<p>In the year 4 of the new era on June the 3rd, Gwynnether von Glanwen is befallen by a strong fever. The cause of this illness had been from severe wounds she caught in a fight against a demon during the Northrop storming. A few days later the fever went down and Gwynnether is reported to be out of mortal danger.</p>

<p>In the year 4 of the new era on June the 4th, the Bloodskull Clan claims the northern territory and forbids trespassing. They make it clear that it is possible roam the lands from the clan's permission.</p>

<p>In the year 4 of the new era on June the 4th, the lizard Shukk is sentenced to prison because of murder investigated by Lyrenzia. The captain of the town guard, Hagen von Rabenfeld, serves to be an advocate with his subordinate Shukk while Aragon (earl of the Grey Rose) accuses Hagen of not executing his duties correctly, stating he had never done anything against the infringements of Shukk. Finally the dispute leads to Hagen demanding a duel with Aragon.</p>

<p>In the year 4 of the new era on June the 10th, Shukk accuses the Grey Rose of having sent the Dan family to assassinate her. The Grey Rose as well as the Dans denies Shukk's accusations.</p>

<p>In the year 4 of the new era on June the 13th, the new tavern keeper (Brodir) seeks assistance for running the Fluffy Sheep Tavern.</p>

<p>In the year 4 of the new era on June the 17th, Igorath Demian (a priest) asks the public to thank the goddess Elara for wisdom, knowledge, and magic. The subject had been issued, regarding the beginning of the month known for Elara.</p>

<p>In the year 4 of the new era on June the 18th, Athian Corulas posts a warning of several larger monster gatherings that he had noticed near the gates of town.</p>

<p>In the year 4 of the new era on June the 18th, Lennier warns everyone about a necromancer named Deidre Rogan who, regarding his words; is bound to roam with monsters and demons. This was evident after one occasion where she was seen summoning dangerous creatures that were later used to attack other inhabitants. Shortly after this, she was imprisoned by the Grey Rose and exorcised by the priests Galim and Fedaykin.</p>

<p>In the year 4 of the new era on June the 20th, a long series of "differences" finds an end. The duel between Aragon ben Galwan and Hagen von Rabenfeld leads to the death of Aragon, slain by Hagen. The loss is overshadowed by Shukk, who was said to be seen spitting on the body of Aragon.</p>

<p>In the year 4 of the new era on June the 21st, Neon Rift and Damien announce that the library belongs to Damien, the Rift family, and the Order of the Grey Light, each obtaining a third between this agreement. It is also said that the library itself will be reconstructed and that large amounts of iron, coal, and wood would be needed for it.</p>

<p>In the year 4 of the new era on June the 23rd, Ultoris invites everyone to an auction of his house by the docks. The auction is cancelled when one of the other owners of the building protests.</p>

<p>In the year 4 of the new era on June the 24th, Fooser publicly swears bloody revenge on Val de Gausse, saying he had been responsible for killing his sheep.</p>

<p>In the year 4 of the new era on June the 24th, Hagen von Rabenfeld (captain of the town guard) states that the members of the Grey Rose no longer have access to the prison. The Grey Rose accepts the order unwillingly.</p>

<p>In the year 4 of the new era on June the 24th, Johann Endan and Ultoris announce the reconcilation and rebuilding of Northerot.</p>

<p>In the year 4 of the new era on June the 27th, Julia Sonnenkind marries Gorden Fisk. They also publicly thank all guests who attended the wedding.</p>

<p>In the year 4 of the new era on June the 28th, Shilly claims that Galim would be her husband as they had married three years ago. It turned out this was nothing more than a bad joke.</p>

<p>In the year 4 of the new era on June the 29th, Willum writes a message to everyone who has been banned from Troll's Bane, encouraging them to think about a life in the wilderness. He asks these people to contact him for the possibility to work together for future housing there.</p>

<p>In the year 4 of the new era on June the 30th, Glosin Ironstar returns from a long voyage.</p>



