<p><a name="5"></a></p>

<h2>-1587 bis -1577 Krieg zwischen Albar und Salkamar</h2>

<p>Die Stadt Albar erklärt Salkamar den Krieg, ein Grund dafür ist unter anderem die
Unterstützung Lor-Angurs durch Salkamar, hauptsächlich jedoch die alte Feindschaft
beider Städte. Gynk verhält sich neutral. Es gibt in den folgenden Jahren mehrere
Scharmützel, in welchen jedoch Salkamaerische Streitkräfte oftmals durch Magier
unterstützt werden. Viele dieser Magier sind ehemalige Albarische Leibeigene, und sehen
in ihrer Unterstützung die Möglichkeit, das Königreich zu schwächen. Der Krieg wird erst
beendet, als ein Albarisches Heer nach dem Versuch, auf einem Umweg am Salkamaerischen
Heer vorbei in die Innenlande einzufallen, zwei Halblingdörfer plündert. Die
Heeresversorgung auf der langen Wanderung sollte durch diese Plünderungen gesichert
werden.</p>

<p>Das Heer scheitert jedoch an der erstaunlichen Zähigkeit und Wehrhaftigkeit der
Halblinge, welcher innerhalb kurzer Zeit ein eigenes, wütendes Heer aufstellen können.
Als sich die zwei Heere gegenüberstehen und zudem sallkamaerische Reiterlegionäre und
Kriegspaladine zur Unterstützung der Halblinge eintreffen, ziehen sich die albarischen
Streitkräfte im Herbst -1577 zurück. Als im Winter die Reserven Albars aufgebraucht sind
und eine erneute Hungersnot eintritt, erwirkt der albarische Botschafter einen
Friedensvertrag, welcher Albar alle sieben Grenzdörfer kostet und die Grenze zwischen
Albar und Salkamar zum ersten Mal seit über hundert Jahren verschiebt. Salkamar beginnt,
mit enormer Unterstützung der ansässigen Bauern, umgehend mit dem Ausbau neuer
Grenzbefestigungen.</p>

