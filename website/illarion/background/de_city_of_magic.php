<h2>-1627 Die Expedition von Nabranoo</h2>

<p>Die Gelehrten von Lor-Angur lassen verkünden, bei Ausgrabungen in einem alten Tempel
auf die Position der legendären goldenen Stadt .Nabranoo. gestoßen zu sein. Eine
Abordnung von Gelehrten, Magiern und Arbeitern bricht in Richtung der Position auf,
kehrt aber gegen Jahresende ohne Ergebnisse zurück. Einige der Forscher bleiben in der
Wildnis, um auf eigene Faust weiterzusuchen. Von ihnen wird nichts mehr gehört.</p>

