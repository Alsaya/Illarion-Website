<h2>-1647 Gründung von Lor-Angur</h2>

<p>Gelehrte Albars finden uralte Ruinen, und stoßen dort auf magische Artefakte und
uralte Formeln. Kurz darauf erklären sie ihre Unabhängigkeit von Albar, und beginnen,
Bürger und Leibeigene mit noch so geringem magischem Talent zu rekrutieren. Die
Albarische Krone entsendet Reiter, welche jedoch auf eine undurchdringliche Barriere
stoßen.</p>

