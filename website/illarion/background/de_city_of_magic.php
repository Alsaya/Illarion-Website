<h2>-1643 Der Wolfswinter</h2>

<p>Mehrere Dörfer im Albarischen Reich werden während eines harten Winters von Wölfen
angegriffen. Sogar an den Grenzen zu Salkamaerischen Landen werden Wölfe gesichtet.
Seltsamerweise greifen viele dieser Wölfe hauptsächlich und methodisch albarische
Adelige und Soldaten an, worauf in Albar umgehend die Magier von Lor-Angur
verantwortlich gemacht werden.</p>

<p>Ein größeres Heer zieht im Frühjahr gegen die anwachsende Kleinstadt, kann jedoch
egen die undurchdringliche Barriere nichts ausrichten. Viele Soldaten desertieren
jedoch, und laufen zur Magierstadt über.</p>

