<p><a name="4"></a></p>

<h2>-1612 bis -1601 Die Belagerung von Lor-Angur</h2>

<p>Die magische Barriere der Stadt, gespeist durch drei im Sonnenlicht aufgeladene,
magische Kristalle, beginnt schwächer zu werden. Da ein Übergriff der albarischen
Truppen befürchtet wird und die Kristalle zur erneuten Ladung mindestens ein Jahr
benötigten, wird die Stadt zur Festung umgebaut. Die Stadt bittet um militärische
Unterstützung durch das Salkamaerische Reich, da sie sich jedoch zu weit in albarischem
Gebiet befindet, wird diese abgelehnt. Der Imperator von Salkamar, Regis Saldor
Negus I., erklärt jedoch Die .Stadt der Magier. als Salkamaerisches Protektorat.</p>

<p>Als die Barriere im Herbst 1603 verschwindet, beginnt ein albarisches Heer mit der
Belagerung. Am Anfang sind die Heeresverluste aufgrund der eingesetzten Magie immens,
a die Stadt-Magier über weitaus bessere und mehr Zauber zu verfügen scheinen als die
Heeresmagier. Das Albarische Heer wird so stark geschwächt, dass es die Belagerung
abbrechen muss. Als die albarische Krone im Frühjahr 1601 ein neu aufgestelltes
Heer schickt, ist die Barriere wieder funktionstüchtig.</p>

<p>Aufgrund der enormen Heereskosten und der wegen Arbeitermangel teilweise nicht
eingeholten Ernten, bricht im darauffolgenden Winter in Albar eine Hungersnot aus.</p>

