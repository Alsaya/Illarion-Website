<p>Both the mouse and the SHIFT key are essential for different crafting and use of items.
 Crafting involves different combinations of items while holding down the shift key and clicking
  on the different things. Food, potions, and other items can be consumed by merely shift-clicking
   on them. </p>

<p>Talking to others within the game is very simple. Just begin typing and press enter to send.
 You will see it appear in a text box before and after pressing enter. The ALT key comes into play
  when working with magic and runes. Pressing ALT opens a mini-screen at the bottom, involved with
   magic and druidry. Another basic control is the ESC key, which is used to exit the client at any 
   time. </p>

<p>Using the left mouse button on an item or object allows you to "see" what the item or object is.
 A good example of this is the signs you will find on the map. Clicking on them allows you to see which
  direction to go to find a location. The left mouse button will also be the main thing used to drag and
   drop items within the client. The right mouse button is mostly involved with opening different storage
    units, like your depot, your bag, and so on. </p>

<p>Some of the other keyboard controls have less to do with the direct gameplay, but still serve some
 functions. The "Home" key on the keyboard can change the color of the text that appears in the client.
