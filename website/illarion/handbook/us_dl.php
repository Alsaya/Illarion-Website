<p>llarion is a free online game based on roleplaying, and you must download the client and go through the account process before starting:</p>

<p>Illarion currently works with Windows 95/98/2000/ME/XP. Though, if you have Windows 95, you need to download Winsock first in order to be able to use the client. The client itself is currently a little less than 3.5MB in total size. To find the link to the client download, and Winsock2 if you have windows 95, go to the downloads page: <br />
<a href="http://illarion.org/illarion/Illarion_WinXP_0.4_build_26.exe">http://illarion.org/illarion/Illarion_WinXP_0.4_build_26.exe</a> (Windows XP/2000 version)<br /> 
<a href="http://illarion.org/illarion/Illarion_Win9x_ME_0.4_build_26.exe">http://illarion.org/illarion/Illarion_Win9x_ME_0.4_build_26.exe</a> (Windows 9X version) <br />
<a href="http://illarion.org/illarion/W95ws2setup.exe">http://illarion.org/illarion/W95ws2setup.exe</a> (Winsock2)<br />
<a href="https://illarion.org/accountsystem/index.php?language=us">Account Process</a></p>


<p>The client comes in a zipped form, and will be needed to be unzipped into a directory of your choice. Extract the files by clicking on the illarion.exe file to whichever area. Later you can eventually make a shortcut onto your desktop. Inside you will see the basic files needed to use Illarion, along with the shortcut to get into the client, once your account has been accepted you should be able to get in. Also, your character folders will also go in there eventually. Inside the character folders will be the log file (if you choose to keep one), and also a names file, which holds all the names of other introduced characters. A loss of this file will result in losing the names in-game as well. If you ever decide to stop playing, Illarion can be removed from your computer with no problems. </p>

<p>The system requirements are: <br />
-Video card with a resolution 1024x768 pixel and color depth 16 bit. <br />
-Microsoft DirectX 7.0 or higher. <br />
-Online Connection. <br />
-Current Illarion Client.<br /> 
-A windows compatible sound card to hear the music and the sound effects. <br />
-Winsock2 to run on Windows 95. </p>

