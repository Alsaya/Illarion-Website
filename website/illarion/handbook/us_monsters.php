<p><b>Flies</b><br />
Flies are a common insect pest on the island. They are generally weak for an experienced fighter,
but for unexpecting travelers they can be a hassle. Flies are most commonly found in swampy areas,
and sometimes in forests. </p>


<p><b>Trolls </b><br />
Trolls are creatures of the forest. They can be found in many forests around the island.
 Trolls are tall and green, to blend in with the forest, and when encountered they can injure 
 travelers, and even warriors. They have been known to attack when their forest is threatened.</p> 


<p><b>Zombies </b><br />
These are some of the weaker undead minions in the world of Illarion. They are most often
 found in dark dungeons, and pose little threat to decent fighters, but they can be a problem
  for some when they gather in number. When defeated they sometimes drop rusty weapons and helmets,
   and sometimes their insides! </p>

 
<p><b>Bandits</b> <br />
Bandits are often unwealthy criminals who operate within the deeper areas of some forests.
 When defeated they sometimes drop their clothing, their daggers, and maybe a shield. They
  prey on unsuspecting travelers going through the forests. </p>

 <p><b>Skeletons </b><br />
This group of boney undead can be dangerous. Most often found in dark dungeons, like other undead. </p>


<p><b>Scorpions </b><br />
Scorpions are found in the desert, quite a ways northeast of the town of Trollsbane.
 They appear small, but can move upon travelers and fighters quickly, and poisoning 
 them with their tails. </p>


<p><b>Ogres </b>< br />
Ogres are large creatures that live in the northern mountains. Their size and choice of
 weapons can make them very deadly to even experienced travelers and warriors.</p>

