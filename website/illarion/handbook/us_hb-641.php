   <h2>6.4.1 The magic-system of a mage</h2> 

   <?php cap(M); ?>

   <p>ages have to know certain magic runes to cast spells. These you can find in magic books,
   which you find or can buy while you play. Afterwards you have read the book successfully you
   will know and adopt the rune in it. To cast a spell hold down &quot;ALT&quot; key. Now there are
   displayed all runes you know in section &quot;D&quot;.</p>

   <p>Then click once on every rune you need for the spell. Spells can consist of one, two or three
   runes. After you have chosen the spell you should chose the target on the map. This can be every
   field on the map. After that you can now release the &quot;ALT&quot; key to cast the spell.</p>

   <p>You can also chose targets if you use a magic wand. If you attack with this weapon (you have
   to hold it in both hands) every spell will be cast - if you don&#39;t choose a target- on your
   opponent (quod vide: &quot;Fight&quot;). Be careful now if you heal! Anyway you can chose
   targets as usual while you got someone in backsight, so that you can heal yourself without
   unselecting the target. Spells need mana to cast them. This mana is displayed in section
   &quot;F&quot;. Mana will only regenerate as well if you eat something - very slow again. To
   accelerate this process you should buy some mana potions from druids and use them if
   required.</p>

