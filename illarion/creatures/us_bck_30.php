   <h2>The Known Creatures in Illarion </h2>

   <p>If you feel tempted to wander past the secure areas of town, you will encounter various
   beasts and creatures which most probably will show their hostility against you. Therefore be
   prepared to fight. If you have never actually taken up a weapon I highly recommend to talk to a
   battle proved warrior or wizard. Heed the advices they are willing to share with you and don’t
   hesitate to run if the danger proves to be greater than expected. The insane person may fight
   until death to know that his abilities weren’t sufficient but the sane one will try to avoid the
   bony grip of the grim reaper.</p>

   <p><img src="insect.gif" align="left" vspace="10" hspace="20" alt="Insekten" /></p>

   <p>Some of the creatures are easy to be disposed of as long as you carry some kind of weapon and
   protection. The inexperienced person will have no problems to fight insects and mummies, as they
   are rather weak. But nevertheless don’t underestimate their danger. Insects are blood sucking
   creatures that tend to live near water but at least inhabit swamp areas. Their sting can be
   quite painful and if they are numerous the loss of blood can eventually be fatal but under
   normal circumstances you should not fear them.</p>

   <p><img src="mummie.gif" align="right" vspace="10" hspace="20" alt="Mumie" /></p>

   <p>Mummies were once members of a secret guild called &quot;The Spirits&quot;. They were devoted
   followers of the goddess Cherga. Not much is known but rumours tell that once in the past they
   betrayed her and a mighty curse has been spoken upon them letting them return after death not to
   find eternal peace. With enviousness they look upon the mortals and in their despair attack them
   when ever they meet them. Most often they can be found near the ruins of forsaken temples where
   they disturb the unwary traveller but their danger is negligible.</p>

   <p><img src="rotworm.gif" align="left" vspace="10" hspace="20" alt="Faulwurm" /></p>

   <p>But there are also other creatures even a well experienced warrior does not want to meet
   without the help of some comrades. Especially in the dark caverns and caves there are beasts
   which seems to have come from the darkest nightmares. One of them is the great rotworm which
   digs its way through the earth with ease. They can get quite large. The biggest rotworm ever
   found had a length of nearly six meter. They live from the rotten remnants of organic material
   but often attack people. It seems that they can locate their prey by sensing the vibrancies of
   moving creatures in the earth. Their teeth are long and razor-sharp inflicting lots of damage
   when penetrating the armour.</p>

   <p><img src="demon.gif" align="right" vspace="10" hspace="20" alt="Demon" border="0" /></p>

   <p>If under unfortunate circumstances you ever will encounter a demon I would recommend to
   quickly pray to a God of your choice and prepare yourself leaving the remnants of your body in a
   hurry. Few are able to withstand the onslaught of a demon and stay alive to tell others about
   it. The origin of the demons is far from this world. They came as servants of Madrook Na’han,
   seemingly invincible monsters prepared to conquer Illarion. Only with the help of the Gods were
   they destroyed in the great battle but some of them escaped and since hide in the deepest caves
   hoping that an adventurer enters their lair. Sometimes when the Bone Lord, their new master,
   calls upon them can they be seen on the surface of Illarion. Avoid them, flee them, as they not
   only take your body but also claim your soul.</p>

