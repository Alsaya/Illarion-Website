   <p>ith the arrival of the new client, a new account system has also followed.
    This new system is based on php scripting and makes for faster results.
	First you must register for an account to be eligible to play Illarion.
	To go to the account registration form, you can either click the large
	&quot;Join Now&quot; button, or click on the <a href="https://illarion.org/accountsystem/index.php?language=us">&quot;Account Request&quot;</a>
	link on the "Illarion" section. </p>

<p>Now you must Request a New Account. Click on the link and enter to fields. Make
 sure your password is not known by anyone else, and do not give it out. The code
 you use now will be the password for all your characters in the future. Once all
 the necessary fields are filled out go back to the "Account Request" page and log in.
 Once you are logged in you must go to your entered email and confirm your account.  
 After you have confirmed your account, you may now take the roleplaying test. </p>

<p>This test makes sure you understand the meaning of roleplay that is needed to play Illarion.
It will ask you to finish a story and ask some more questions. Answer them to the best of your
ability and thoroughly. If you have passed the test you will recieve an email telling you have 
done so.</p> 

<p>With the test passed, now you can create some characters. I would advise to have only one
to start out with. You do not want to get overloaded while learning the ropes of Illarion. Click
the "Create New Character" in your account log in page. This will link you to a page with a few
fields you must fill out.</p> 
 
 <div class="center"><table bordercolor="#1F1B76" border="1"><tr> <td><img src="layout_create_char.jpg" alt="Picture of Requesting Character Page."/></td></tr></table></p>

<p>When choosing a name, think of a the race and sex of your character before hand.
You do not want and orc going around with an elven name (most likely the name would not be accepted).
The character name should fit the race and sex. Once you have filled out the fields you must wait
for the name to be accepted. If you followed the Name Rules, then you should be fine. </p>

<p>Now you have to create attributes for you new character once his name has been accepted.
Attributes can not be changed later so think carefully while creating this.</p> 

<p>Here are the attributes and their meanings:</p> 

* Agility: The speed of your character. <br />
* Constitution: The resistance to health you character has. <br />
* Dexterity: Hand and Eye coordination. <br />
* Essence: Natural resistance against magic.<br /> 
* Intelligence: How smart your character is. <br />
* Perception: How well your character can use his senses. <br />
* Strength: How strong your character is. <br />
* Willpower: How charasmatic your character is. <br />
* Weight: How much your character weighs. <br />
* Height: How tall your character is. <br />

<p>These traits may effect your character in an obvious way, and others may not.
 Use common sense when filling out this part. If you focus on one section then you
 character will be weak in another. This is not necessarily a bad thing, but there is
 no such thing as a perfect character. It all depends on your plans for the character 
 being created.</p>

<p>Once everything is done, you can go play illarion. Remember to abide by the rules and make
it enjoyable for everyone ingame as well as you. This is a game, so remember to have fun.
If you want to create another character, follow through this tutorial but skip over the
roleplaying test. Remember to read over the rest of the handbook.</p>

