   <div class="center"><img src="depot.png" alt="A Depot"/></div>
   <p>This is a depot. You will find these in strategic locations in different towns, and possible 
   elsewhere. They are basically boxes with limited space, in which you can store the items you make,
    buy, and gather. These items can be put in and removed when you find another depot. The depot is
	 for safe storage, no one else can take things from your depot. To open one, simply walk next to it,
	  and right click on it. A mini-screen should appear at the bottom of the screen with little squares
	   where you can put your things. If you have any items there, they will also show up.</p>
	   
	  <div class="center"><img src="bag.png" alt="A Bag"/></div>
 <p>Everyone starts out with a bag. They are useful for carrying extra, because you only have very limited
  carrying capacity without one. Bags work similarly to depots, except you travel with them. Simply right
   click on the bag in inventory square, or wherever else the bag is, and it show open to place or remove
    items. Unlike depots though, items can be taken from them if you place a bag on the ground. Also be
	 careful of the weight which you are carrying. </p>
	 
	
	<div class="center"><img src="chest.png" alt="A Chest"/></div>
    <p>These boxes are different from depots. Their most useful purpose it to trade items between players.
	 Items place in one of these boxes can be removed by anyone. They also work by right clicking on them,
	  then drag and drop the items with your mouse. </p>
	  
	 <div class="center"><b>Using and Gathering Items</b></div>
   <p>There are many places in Illarion where you can "take" things. This includes apple trees,
    cherry trees, farming, and much more. When you see a cherry tree or apple tree for example,
	 you must walk up next to it, then hold down the shift key, and left click on it, and the apple
	  or cherry will fall to the ground, and can be picked up by dragging and dropping it into your
	   inventory. Using items is also important for all crafts in Illarion. This usually requires different
	    combinations of shift-clicking on different materials and tools (and your character normally 
		has to hold the proper tools in his hands). To learn more about how to do these things, try 
		experimenting, or even better: try asking an experienced craftsman in-game.</p>
		
		
   
   
   
   
   
   

