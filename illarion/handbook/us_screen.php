<p>2- This is the text box. When you are speaking with other characters, the text previously spoken will appear here. You may review recent character dialogue by pressing Page Up or Page Down on your key board. You may also change the colour of the text displayed here by pressing the Home key on your key board. As well as inter player speech, announcements, server announcements and hints and tips for quests and events will appear here. 
</p>
<p>3- This is your mana bar. When you run out of mana, you will be unable to cast spells, create bardic songs, or use priest magic. It can be replenished by drinking mana potions and also replenishes naturally depending on your character’s Essence skill. 
</p>
<p>4- These are your six belt slots. You may place items here, but because they are limited, the number of items you can carry on your belt is also limited. It is recommended that you carry essential items here, like mana potions and so forth. 
</p>
<p>5- This is the Counter. By clicking up or down on the counter, you can alter the number displayed. If items are stacked, and you wish to pick up more than one at a time, simply adjust the counter, to the number of items you wish to pick up. It can also be used to read pages of books. The number displayed on the Counter corresponds with the page number of the book. It can also be used to move through your depot, bag and other containers and menus. 
</p>
<p>6- This is your health bar. When this reaches zero your character will die and you will have to seek a way to be revived. Some mages have the ability to do this, but the easiest way is to go to the Yellow Cross. There are several located on the map. Drink health potions to replenish this. It also replenishes naturally depending on your character’s Constitution skill. 
</p>
<p>7- This is the mini-map. It displays the surrounding area. The coloured areas are those that you have already been to. The black areas are those that you have not been to yet. 
</p>
