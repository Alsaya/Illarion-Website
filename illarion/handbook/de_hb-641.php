   Zaubers auf der Karte ausw&auml;hlen. Dies kann jedes Feld der Karte sein. Danach erst lasse wieder
   die ALT-Taste los, um Deinen Zauber zu sprechen.</p>

   <p>Du kannst aber auch Ziele ausw&auml;hlen, indem Du als Zauberer einen Zauberstab benutzt. Wenn Du
   mit dieser Waffe angreifst (Du musst sie mit beiden H&auml;nden halten), wird jeder Zauberspruch -
   bei dem Du kein Ziel w&auml;hlst - auf Deinen Gegner gesprochen (siehe auch: &quot;Kampf&quot;). Sei
   also nun vorsichtig beim Heilen! Trotzdem kannst Du wie &uuml;blich auch andere Ziele ausw&auml;hlen,
