   kannst, ohne erst das eigentliche Ziel der Spr&uuml;che wieder abw&auml;hlen zu m&uuml;ssen. Zauberer ben&ouml;tigen
   Mana um Ihre Spr&uuml;che zu bewirken. Dieses Mana wird in Sektion &quot;F&quot; gezeigt. Auch Mana
   regeneriert sich nur dann, wenn Du etwas isst - wiederum sehr langsam. Um diesen Prozess zu
   beschleunigen, solltest Du von einem Druiden ein paar Mana-Tr&auml;nke kaufen und diese bei Bedarf
   einnehmen.</p>

