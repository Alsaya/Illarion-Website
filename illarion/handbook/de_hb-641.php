   <p>agier m&uuml;ssen bestimmte magische Runen kennen, um zu zaubern. Diese stehen in Zauberb&uuml;chern,
   die Du w&auml;hrend des Spieles finden wirst oder kaufen kannst. Nachdem Du das Buch erfolgreich
   gelesen hast, wirst Du die darin befindliche Rune kennen und einsetzen k&ouml;nnen. Um einen Zauber
   zu sprechen, dr&uuml;cke die Taste &quot;ALT&quot;. Nun werden alle Dir bekannten Runen in den
   Sektionen &quot;D&quot; gezeigt.</p>

   <p>Dann klicke auf jede Rune, die Du f&uuml;r einen Spruch ben&ouml;tigst. Zauber k&ouml;nnen aus einer, zwei
