   <p>llarion is a graphic online role playing game, where several players can play with each
   other. You can create a character for yourself to pass many adventures in our virtual
   fantasy-world. The most of the other characters you will meet in Illarion are also
   user-controlled characters, NPC&#39;s (non playing characters) are an exception. You can speak,
   trade and experience many adventures with the other inhabitants of Illarion.</p>

   <p>The role playing itself forms the centre of this game. This is the reason why you have to
   make up a background story, this will make it easier to start a conversation with other
   inhabitants. Please don&#39;t use known stories from movies, television, novels or games and
   don&#39;t re-enact known figures. Come up with something of yourself!</p>

   <p>There is a difference to other games, this game has no beginning, no end, no winner and not a
   high score-list. Everything is in change, also your character will enhance. If you enter the
   game for your first time, it will be the same like you are a traveller who is the first time on
   this island. You don&#39;t know anyone and everything is new and strange to you.</p>

   <p>You have to learn much. Your skills will only rise, if you practice them. You will only
   become a good goldsmith by a patient handling with goldsmith works.</p>

   <p>Something important, before we start: Illarion is not a commercial online-game. This means on
   the one hand, you will not have any costs except the online-fees, but on the other hand you are
   not entitled to play Illarion. There could arise downtimes because of maintenance work at the
   server. The Illarion-team is always amenable for proposals, suggestions, problem reports etc.,
   but always remember not to demand something, but to
   suggest.</p>

