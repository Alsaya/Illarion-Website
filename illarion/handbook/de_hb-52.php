   <h2>5.2 Steuerung</h2>

   <p>Um sich in der Welt von Illarion zu bewegen, gibt es drei M&ouml;glichkeiten:</p>

   <ul>
      <li>Als erstes die Cursortasten: dr&uuml;cke einfach die Richtung, in die Du gehen m&ouml;chtest.</li>

      <li>Dein Charakter wird sich bewegen, bis Du die Taste wieder losl&auml;sst.</li>

      <li>Du kannst auch einen Doppelklick in das Zielfeld auf der detaillierten Karte (Sektion
