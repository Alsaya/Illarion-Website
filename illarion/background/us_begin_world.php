<h2>-5,200: Scriptures</h2>

<p>Written elvish documents were found around -900 and are dated back to an origin of
time of around -5,200. These scriptures are considered to be the first scriptures,
inscribed upon leathery parchment-thus being transportable and flexible. The first
discovered scripture is apparently an ancient poem or song; a written warning in verse
form:</p>

<p>Many a wanderer's path that leads into darkness<br />
By the shadows of Nabranoo.<br />
Many a stony ridge has bound the bundle<br />
By the shadows of Nabranoo.<br />
Ruin is witness of sacrilegious deed,<br />
To avoid the darkness is council one doth need,<br />
Many a wanderer's heart, seduced in the darkness<br />
By the shadows of Nabranoo.</p>

<p>There is no historic documentation yet. All discovered scriptures or tablets from
before -5,200 are exclusively clay and stone tablets, cave wall paintings and frescoes,
all of which are surely too unpractical for scribing longer texts. They serve solely for
organizatory purposes. Additionally, there is no real record of years; apparently it had
been deemed unnecessary.</p>

<p>The first historic event that can be dated exactly by the means of reconstruction
through records, are the beginnings of the orcish wars. These records were made by
elvish hands and copies of these documents can be found in the archives of Salkamar.</p>

