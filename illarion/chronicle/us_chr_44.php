<p>In the year 4 of the new era on April the 19th, Brendan Mason announces the death of Viola Thistle. She is buried five days later.</p>

<p>In the year 4 of the new era on April the 20th, Fedaykin goes to Falkner's shrine for prayers, since faces of the seer Arslan disconcert him. Fedaykin is later reported to disappear in mysterious ways and later reappears on May 15th, calling himself a priest of Oldra.</p>

<p>In the year 4 of the new era on April the 22nd, Val de Gausse announces the founding of a new group called "The Setinels." However Hagen von Rabenfeld refers to the surrender regulations, which forbid Val being a member of a guild. Val ignores these regulations and looks for further fellow combatants.</p>

