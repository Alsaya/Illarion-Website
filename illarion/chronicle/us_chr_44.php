<p>In the year 4 of the new era on April the 6th, the halfling Thomas Oakridge is kidnapped by orcs. Moments later, Lasendor El&uacute;f also disappears from the streets of Troll's Bane.</p>

<p>In the year 4 of the new era on April the 8th, Fieps announces his planned wedding with Zasmia. Unfortunately, it takes place a week later than planned.</p>

<p>In the year 4 of the new era on April the 9th, Drathe requests assistance in finding the new method to craft wands. In his opinion, the knowledge should only be granted to mages with dignity. Nilo offers to show him the method, since it is already well known to the public.</p>

<p>In the year 4 of the new era on April the 10th, the orc Gorock (of the Bloodskull Clan) kills his clan mate and shaman, Kaja Wolfagen. With a great miracle and luck, Kaja is alive once again two weeks later.</p>

<p>In the year 4 of the new era on April the 12th, Grunith Shuth announces the freeing of two orc brothers from enslavement.</p>

<p>In the year 4 of the new era on April the 14th, Aryllis arrives on the island and heads for the Eldan Monastery, seeking to enter it as a novice.</p>

<p>In the year 4 of the new era on April the 16th, Val de Gausse looks for mages, archers, and fighters; seeking to form a group that requests payment for supporting customers in fights (or to simply escort them to certain destinations).</p>

<p>In the year 4 of the new era on April the 17th, an anonymous message appears at the shop wall, claiming that the town guard has killed citizens that literally kick members of the town guard. One guard (Nedor von Largon) is said to carry out an attack on a "crazy woman", whereupon he is criticized from all sides. A further anonymous message appears a little later, which mocks all members of the town guard.</p>

