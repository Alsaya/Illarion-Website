<p>In the year 4 of the new era on April the 18th, Willum Wildthorn demands to be released from the prison, reporting to everyone that he had surrendered and the war against the movement had ended. One day later, Hagen von Rabenfeld frees him.</p>

<p>In the year 4 of the new era on April the 18th, Elise Gutenhof is saved from drowning and thanks her rescuer, whereupon Toshkan gives a cynic comment.</p>

