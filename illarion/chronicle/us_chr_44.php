<p>In the year 4 of the new era on April the 2nd, the war council decides (in the matter of struggle with Darlok) to dig a tunnel to Darlok's castle. Gandela, Galim, Lennier and Borek attempt to complete this task; however, Darlok notices their attempt and the exit to the tunnel collapses. The enclosed workers are able to free themselves not before the 12th of May through a troll's cave.</p>

<p>In the year 4 of the new era on April the 4th, Thomas Weathergood requests assistance in getting around on the island because he's new to it. Later, it becomes clear that it's Paul Laffing who had simply changed his identity.</p>

