<h2>Weapon/armour strength</h2>

<ul>
	<li class="question">How do I know the precise weapon strengths?</li>

	<li class="answer">You don't. We try to make Illarion as realistic as possible, and in
	medieval times, you simply would not hear a weapon smith say "This sword has an attack
	value of 20". Instead people tried out several swords, and decided then which one suited
	them best. The weapons do have specific values, but those are decided by many factors, i.e.
	the skill of the smith, and the quality of the ore the item was made form, so we cannot give
	you a numerical value anyway. Some people have been complaining that you should at least be
	able to see rough values, but for this, simply think. A dagger does not inflict as much pain
	as a battle axe, but it is much faster. A short sword would be somewhere in between.</li>
</ul>

