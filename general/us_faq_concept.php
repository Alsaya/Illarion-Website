<h2>Skill values/attributes</h2>

<ul>
	<li class="question">Where can I see my exact skill levels and attributes?</li>

	<li class="answer">Once again, we stress realism in Illarion. A person cannot be reduced to a
	numerical value. Just imagine, you sit on a bench somewhere and overhear a conversation. A
	person says " I increased my running skill to level 40 yesterday, and levelled up to
	level 43." I am sure you would laugh pretty hard at that person if he said that. Well,
	the same counts in Illarion. You can see rough values in the form of a bar graph in the skill
	list (as you can always compare yourself roughly to other people). Also, it is very annoying
	to always hear someone bragging about their skills, wherever you go. Imagine this; you are
	sitting in the tavern, enjoying a chat with a close friend about the new politics of the
	elderly king. Suddenly someone behind you starts yelling that he is level 10 in carpentry,
	and can thus make good items for you. It does not get much more annoying than that. It would
	be simpler to have a precise skill system, but we did create this game in the spirit of Role
	playing. If you disagree with this, Illarion might not be the game for you.</li>
</ul>

