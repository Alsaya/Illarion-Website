<h2>RPG/Role playing?</h2>

<ul>
	<li class="question">Every one keeps talking of RPG and Role playing. What is that?</li>

	<li class="answer">Role playing games (RPG) are a popular genre of games, where players
	immerse themselves completely into the life of their character. You cease to be yourself, and
	instead lead the life of a fictional character feeling, saying and thinking what he would. In
	the case of Illarion, the world is a medieval fantasy realm with orcs, dwarves magic and
	monsters. Your character could be a renowned warrior, a potent mage, a rich craftsman, or
	even a poor beggar groveling in the gutter. The choice is all yours. An important aspect of
	role playing is the character interaction that is bound to happen. Whether you are a merchant
	offering his wares, A pompous baron oppressing the peasants, or a warrior seeking to prove
	his worth; Become your character. Speak, think and act as he/she would. If you are a peasant,
	speak with a crude accent. If you are grand mage, make yourself appear mystical and eerie. If
	you immerse yourself completely in your character, you will truly become him, feeling his
	highs and lows. Who knows, you might even fall in love and marry? Another source of Role
	playing is the rpg boards in the Illarion forum. On these you can organize or participate in
	virtually anything. The other players will be more than happy to help you, whether you wish
	to go on a quest to find your estranged parents, or you want to organize your own wedding.
	Let them interact and influence your character's life, and try to play a part in their
	story. There are virtually no limits to what you can do! A rule to observe though: Keep the
	posts on the rpg and guild boards IC (in Character) i.e. Speak as you would in the game - as
	your character does. OOC (Out of character; i.e. how you, the person behind the screen of
	your pc, would speak) Is allowed on the other boards. All this is to promote Role playing.
	Many other games that call themselves rpg's, do not actually feature any role playing. If
	this concept does not interest you, then Illarion is probably not the game for you. (for more
	information, have a look at "About the project")</li>
</ul>

