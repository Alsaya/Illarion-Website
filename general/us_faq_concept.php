<h2>Global chat</h2>

<ul>
	<li class="question">How do I send global chat messages?</li>

	<li class="answer">You can't. How would you do that in medieval times anyway? There were
	no P.A. systems, or radios so such things were impossible. We might grant powerful mages a
	special ability to communicate by telepathy but nothing is planned yet. If you need to spread
	a message, you can always hire another player as a town crier.</li>
</ul>

