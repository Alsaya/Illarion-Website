<h1>Warum die Arbeit und was zeichnet Illarion so besonders aus?</h1>

<?php cap(U); ?>

<p>nser Ziel ist es, ein komplexes Online-Rollenspiel (multi user dungeon) zu erstellen.
Einige werden sicherlich aufstöhnen "Schon wieder ein MUD ... es gibt doch schon so
viele. Warum programmieren die nicht an einem schon vorhandenen Projekt mit?" Die
Antwort ist recht einfach: Ein Informatikstudent (wie wir es bei Projektbeginn an der
Uni waren) sollte sich mit Netzwerkprogrammierung, Threads, KI (künstliche Intelligenz),
Softwareentwicklung im Team, allgemein objektorientierte Programmierung usw. ein wenig
auskennen. Diese Dinge (und eine ganze Reihe mehr), verknüpft mit unserem Hobby der
Rollenspiele, führen fast unweigerlich zu dem Schluss, selbst ein solches Spiel
programmieren zu wollen. Es geht uns also um den Spass und den Lerneffekt dabei -
deswegen wollen wir nicht an etwas Vorgefertigtes ran. Für einen Erfahrungsaustausch mit
anderen Projekten sind wir selbstverständlich immer offen.</p>

<p>Besonderes Augenmerk wollen wir dabei darauf legen, dass in unserem Illarion
richtiges Rollenspiel möglich ist und sich die Handlungen nicht nur auf bloßes
"Monsterverprügeln" beschränken. Dazu haben wir (hoffentlich) die Berufe so gestaltet,
dass einigen Berufen ein solches Leben auch gar nicht möglich wäre und dass sich
Charaktere verschiedener Berufe zu Gruppen zusammenfinden müssen, um ihre Ziele zu
erreichen, weil sie nur in der Kombination ihrer Fähigkeiten ihre Stärken voll
ausspielen können.</p>

<p>Wir versuchen ein Spiel zu entwickeln, das besonders das Rollenspiel betont. Dies ist
unserer Meinung nach bei den meisten Spielen unterentwickelt, und zeigt sich mehr in
liebevollen Details als in großen technischen Innovationen (obwohl Illarion auch von der
Technik her nicht zu den schlechtesten des Genres gehört).</p>

<p>In den meisten Spielen kann man z.B. die Namen aller Spieler sehen, was häufig zu
Unmut führt und unlogisch ist. In unserem Spiel muss man sich anderen erst vorstellen,
damit sie den eigenen Namen dauerhaft kennen und angezeigt bekommen.</p>

<p>Das Magiesystem ist sehr komplex: Es gibt unterschiedliche Runen, jede mit eigener
Bedeutung. Aus der Kombination der Runen kann der Spieler Zauber selbst erforschen (bei
über 4 Milliarden theoretisch möglichen Runenkombinationen). Die Runen wird man einzeln
aus Büchern oder von Lehrmeistern erlernen müssen. Die Erfolgschancen beim Zaubern
richten sich nach den Fertigkeitswerten für jede verwendete Rune.</p>

<p>Druiden werden überall nach Kräutern suchen können und - je nach Klimazone und
Bewuchs - unterschiedliche Pflanzen finden, wenn ihnen ein weiser Druide vorher das
Aussehen und die Bedeutung der Pflanze erklärt hat. Er muss dann wieder Kombinationen
erforschen, um sinnvolle Tränke herstellen zu können.</p>

<p>Da es keine Erfahrungspunkte gibt, kann man auch ein erfolgreiches Leben führen, ohne
Monster zu erschlagen; allein durch das Ausüben zahlreicher Handwerke.</p>

<p>Es wird ein Gesellschafts-System geben. Die Stellung in diesem System richtet sich
nach dem Luxuskonsum. Wer also viel Gold in teure Kleidung und Essen investiert, kann so
in wichtige Ämter gelangen. Dadurch werden z.B. Lehnsherren-Beziehungen möglich, wenn
ein reicher Spieler Steuern von anderen nimmt, und sie dafür vor Gericht vertritt.</p>

<p>Es ist möglich, Spieler K.O. zu schlagen, ohne sie zu töten. So kann man z.B.
sinnvoll einen Wegelagerer spielen.</p>

<p>Häufig gibt es auch Ärger, wenn Spieler im Spiel über ihre Attributswerte und Punkte
reden, hier kennt der Spieler diese gar nicht bzw. nur in ihrer Tendenz.</p>

<p>Bei vielen grafischen Online-Spielen kann der Spieler sein Volk nicht bestimmen, hier
gibt es verschiedene Völker mit eigener Grafik und eigenen Städten zur Auswahl (z.B.
leben Elfen in Baumstädten, Echsenmenschen in Unterwasserstädten,
Zwerge unterirdisch ...).</p>

<p>Viele Aktionen und Zauber werden die Gesinnung eines Spielers ändern. So senkt z.B.
das Töten eines Einhorns oder Beschwören eines Untoten die Gesinnung, das Erlegen eines
Vampirs oder Heilen eines Charakters steigert sie. Die Gesinnung wird den Erfolg bei
Zaubersprüchen mit beeinflussen, da jeder Zauber einen Gesinnungswert hat (d.h. z.B.
für böse Charaktere ist es einfacher böse Zauber zu sprechen).</p>

<p>Dies sind nur einige, von vielen Beispielen, wie wir Rollenspiel fördern wollen. Noch
nicht alles ist verwirklicht, denn wir sind noch in der Entwicklung. Ihr seid also
herzlich in die Welt Illarions eingeladen und gebt eure Kommentare und Vorschläge auf
dem Forum ab.</p>

